def duel_algo(user1, user2):
    all_rate = user1.rate + user2.rate
    w1 = int(user1.rate / all_rate * 100)

    # —á—Ç–æ–±—ã –Ω–µ –±—ã–ª–æ –¥—É—ç–ª–µ–π 100-0
    if w1 == 0:
        w1 = 1
    elif w1 == 100:
        w1 = 99

    w = randint(1, 100)
    m1 = w1
    res = dict()

    if w <= m1:
        res['winner'] = user1
        res['wr_w'] = w1
        res['loser'] = user2
        res['wr_l'] = 100 - w1
    else:
        res['winner'] = user2
        res['wr_w'] = 100 - w1
        res['loser'] = user1
        res['wr_l'] = w1

    return res


# –§–æ—Ä–º—É–ª–∞ —Ä–∞—Å—á—ë—Ç –¥–µ–Ω–µ–≥ –Ω–∞ –ø–æ–±–µ–¥—É
def calculate_money_win(wr1, wr2, money1: float, money2: float) -> float:
    money_win = min(wr1, wr2) * min(money1 / 100, money2 / 100)

    if wr1 < wr2:
        money_win *= 1.1

    return to_two_digits(money_win)


