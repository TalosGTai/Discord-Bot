# –Ω–∞ –≤—Ö–æ–¥ –ø–æ–¥–∞—ë—Ç—Å—è —á–∏—Å–ª–æ (—Ö –∏–ª–∏ —Ö—Ö) -> xx
def time_format(time: str) -> str:
    if len(time) == 1: return '0' + time
    return time


# delete \
def delete_reverse_slash(s: str) -> str:
    if s.find('\\') == -1:
        return s
    return s[:s.find('\\'):] + s[s.find('\\') + 1::]


